    private void guardarServicio() {
       Servicio ser = new Servicio();
       ser.setNombre(tfNombre.getText());
       ser.setDescripcion(tfDescripcion.getText());
       ser.setValorUnitario(Double.parseDouble(tfValorUni.getText()));
       if(cbActivo.isSelected()){
            ser.setEstado(true);
        }
        if(cbInactivo.isSelected()){
            ser.setEstado(false);
        }
        servicioDao.insertarServicio(ser);
        JOptionPane.showMessageDialog(this, "Servicio registrado con exito", "Aviso", 1);
        limpiarCampos();
    }
    private void limpiarCampos(){
        tfCodigo.setText("");
        tfNombre.setText("");
        tfDescripcion.setText("");
        tfValorUni.setText("");
       
    }

    private void consultar(int codigo) {
        if(codigo != 0){
           Servicio ser = servicioDao.consultar(codigo);
           
           if(ser != null){
               tfNombre.setText(ser.getNombre());
               tfDescripcion.setText(ser.getDescripcion());
               tfValorUni.setText(String.valueOf(ser.getValorUnitario()));
           } else{
           JOptionPane.showMessageDialog(this, "No existe cliente con este codigo!","Aviso",1);
           }
       }else{
          JOptionPane.showMessageDialog(this, "Debes digitar un numero!","Aviso",2); 
       }  
    }

    private void modificar() {
         Servicio ser= new Servicio();
         ser.setCodigo(Integer.parseInt(tfCodigo.getText()));
         ser.setNombre(tfNombre.getText());
         ser.setDescripcion(tfDescripcion.getText());
         ser.setValorUnitario(Double.parseDouble(tfValorUni.getText()));
         
         servicioDao.modificar(ser);
         JOptionPane.showMessageDialog(this, "Servicio actualizado con exito", "Aviso", 1);
        limpiarCampos();
    }

    private void eliminar(int codigo) {
          int opcion= JOptionPane.showConfirmDialog(this, "Estas seguro que quieres eliminar..?","Aviso", JOptionPane.YES_NO_OPTION);
        if (opcion==JOptionPane.YES_OPTION){
            servicioDao.eliminar(codigo);
            JOptionPane.showMessageDialog(this, "Servicio se ha borrado con exito", "Aviso", 1);
            limpiarCampos();
            
        }
    }
